let diasemana = Number(prompt("Ingrese un numero (1 al 7): "))


switch (diasemana) {
    case "1":
        console.log("Es lunes")
        break
    case "2":
        console.log("Es martes")
        break
    case "3":
        console.log("Es miercoles")
        break
    case "4":
        console.log("Es Jueves")
        break
    case "5":
        console.log("Es viernes, ea")
        break
    case "6":
        console.log("Es sabado, eaaa")
        break
    case "7":
        console.log("Es domingo")
    default:
    console.log("Coloca un numero valido")
    break
}
